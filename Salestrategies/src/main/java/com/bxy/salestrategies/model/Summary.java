package com.bxy.salestrategies.model;

public class Summary {
	private int id;
	private int opportunity_id;
	private String vertial_market;
	private String compelling_mechanism;
	private String our_solution;
	private String our_quantified;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getOpportunity_id() {
		return opportunity_id;
	}
	public void setOpportunity_id(int opportunity_id) {
		this.opportunity_id = opportunity_id;
	}
	public String getVertial_market() {
		return vertial_market;
	}
	public void setVertial_market(String vertial_market) {
		this.vertial_market = vertial_market;
	}
	public String getCompelling_mechanism() {
		return compelling_mechanism;
	}
	public void setCompelling_mechanism(String compelling_mechanism) {
		this.compelling_mechanism = compelling_mechanism;
	}
	public String getOur_solution() {
		return our_solution;
	}
	public void setOur_solution(String our_solution) {
		this.our_solution = our_solution;
	}
	public String getOur_quantified() {
		return our_quantified;
	}
	public void setOur_quantified(String our_quantified) {
		this.our_quantified = our_quantified;
	}
	
}
